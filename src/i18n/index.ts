const dictionaries: Record<Language, Dictionary> = {
  'zh-CN': zhCN,
  'en-US': enUS
};

export function translate(language: Language, key: string, vars?: Record<string, string | number>) {
  const template = dictionaries[language][key] || zhCN[key] || key;
  if (!vars) return template;
  return Object.entries(vars).reduce(
    (result, [name, value]) => result.replaceAll(`{${name}}`, String(value)),
    template
  );
}

export function useI18n() {
  const language = useAppStore((state) => state.data?.settings.language || 'zh-CN');
  const t = (key: string, vars?: Record<string, string | number>) => translate(language, key, vars);
  return { language, t };
}
